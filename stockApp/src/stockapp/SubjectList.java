    public void loadSubjects()
    {
         String[] headings ={"Subject Id","Subject Name","Category Name"};
        DefaultTableModel dtm = new DefaultTableModel(headings, 0);
        jTable1.setModel(dtm);
        try
        {
            Statement st=StockApp.con.createStatement();
            ResultSet rs= st.executeQuery("Select subjects.subjectId, subjects.subjectName, category.categoryName from subjects, category Where subjects.categoryId=category.categoryId");
            
            while(rs.next())
            {
                
                Object[] row = new Object[3];
                
                row[0] = rs.getInt("subjectId");
                row[1] = rs.getString("subjectName");
                row[2] = rs.getString("categoryName");
                
                
                dtm.addRow(row);
                
            }//While

        }//try
                    
    catch(Exception ex)
    {
        JOptionPane.showMessageDialog(rootPane, ex);
    }//catch
    }
        
    

   
