    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int x=jTable1.getSelectedRow();
      if(x>=0)
      {
          int temp=Integer.parseInt(jTable1.getValueAt(x,0).toString());
          category obj=new category(null,true);
          obj.startEditingCategory(temp);
          obj.setVisible(true);
          loadCategory();
          
      }//if
                 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int a=jTable1.getSelectedRow();
        
        if(a>=0)
        {
            int wid =  Integer.parseInt( jTable1.getValueAt(a, 0).toString());
            
            try
            {
                PreparedStatement ps = StockApp.con.prepareStatement("Delete From Category where CategoryId=?");
                ps.setInt(1, wid);
                ps.execute();
                
                loadCategory();
            }//try
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }//catch
            
        }//if
        
    }//GEN-LAST:event_jButton3ActionPerformed

    
