    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     boolean ret=validateCategory();
     if(ret==true)
     {
        try{
          if(categoryId==0)
          {
          PreparedStatement ps=StockApp.con.prepareStatement("insert into category values(?)");
          ps.setString(1,jTextField1.getText());
          ps.execute();
          }//if
          else
          {
              PreparedStatement ps=StockApp.con.prepareStatement("Update category set categoryName=? where categoryId=? ");
              ps.setString(1,jTextField1.getText());
              ps.setInt(2,categoryId);
          }//else
          
      }//try
      catch(Exception ex)
      {
          JOptionPane.showMessageDialog(rootPane, ex);
          
      }//catch
        JOptionPane.showMessageDialog(rootPane, "Record saved");
     }//if
     jTextField1.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

