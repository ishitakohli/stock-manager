       private void loadAuthors()
       {
        try 
        {
          String[] headings={"AuthorId","First Name","Last Name"};
          DefaultTableModel dtm=new DefaultTableModel(headings,0);
          jTable1.setModel(dtm);
          Statement st=StockApp.con.createStatement();
          ResultSet rs=st.executeQuery("Select * from authors");
          while(rs.next())
          {
              Object[] row=new Object[3];
              row[0]=rs.getInt("authorId");
              row[1]=rs.getString("firstName");
              row[2]=rs.getString("lastName");
              dtm.addRow(row);
          }//while
          
          
        }//try
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }//catch
    }

    
