    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean ret=validateAuthors();
        if(ret==true)
        {
                try
                 {
                     if(authorId==0)
                     {
                        PreparedStatement ps=StockApp.con.prepareStatement("insert into authors values(?,?)");
                        ps.setString(1,jTextField1.getText());
                        ps.setString(2,jTextField2.getText());
                        ps.execute();
                       jTextField1.setText("");
                       jTextField2.setText("");
                       JOptionPane.showMessageDialog(rootPane, "Author added successfully...");
                     }//if
                    else
                    {
                        PreparedStatement ps=StockApp.con.prepareStatement("Update authors set firstName=?,lastName=? where authorId=?");
                        ps.setString(1,jTextField1.getText());
                        ps.setString(2, jTextField2.getText());
                        ps.setInt(3,authorId);
                        ps.execute();

                        JOptionPane.showMessageDialog(rootPane, "Author record updated successfully...");
                        this.dispose();
                    }//else
                }//try
                catch(Exception ex)
                {
                   JOptionPane.showMessageDialog(rootPane, ex);
                }//catch
            
        }//if

    }//GEN-LAST:event_jButton1ActionPerformed

