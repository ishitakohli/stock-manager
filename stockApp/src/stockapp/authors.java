     private boolean validateAuthors()
     {
         if(jTextField1.getText().length()==0)
         {
             JOptionPane.showMessageDialog(rootPane,"First Name is empty", null,JOptionPane.WARNING_MESSAGE);
             return false;      
         }
         if(jTextField2.getText().length()==0)
         {
             JOptionPane.showMessageDialog(rootPane,"Last Name is empty", null,JOptionPane.WARNING_MESSAGE);
             return false;
             
         }
         return true;
     }
   
    public void startEditingAuthors(int aid)
    {
        try
        {
            authorId=aid;
        
            PreparedStatement ps=StockApp.con.prepareStatement("Select * from authors where authorId=" +authorId );
            ResultSet rs=ps.executeQuery();
            rs.next();

            String s1=rs.getString("firstName");
            String s2=rs.getString("lastName");
            rs.close();
            ps.close();
            jTextField1.setText(s1);
            jTextField2.setText(s2);
       
        }//try
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }//catch
    }//func

